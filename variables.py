# Python program to demonstrate variables

# Assigning values to variables
name = "Mahi"
age = 21
height = 5.9
is_student = True

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student:", is_student)

# Changing variable values
age = age + 1   # age increases by 1
print("Next year age:", age)

# Multiple assignments
x, y, z = 10, 20, 30
print("x:", x, "y:", y, "z:", z)

# Same value for multiple variables
a = b = c = 100
print("a:", a, "b:", b, "c:", c)
